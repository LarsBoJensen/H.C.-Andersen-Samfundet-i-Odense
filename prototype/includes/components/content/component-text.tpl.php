<div class="component-text">
  <p>
    <strong>
      Eine Familie mit Kindern die SU (Statens Uddannelsesstøtte) erhält, steht
      vor der schweren Herausforderung, die Balance zwischen Stundenplan und
      Familienleben zu meistern. Aus diesem Grund ist es besonders kritisch,
      wenn die Ausbildungsunterstützung für Studierende mit Kindern gekürzt wird,
      so der Direktor  der Organisation Mødrehjælpens, Mads Roke Clausen, zum
      Vorhaben der Regierung, in ihrem 2025-Plan, die SU sowohl für Menschen mit
      Handicap, als auch für Studierende mit Kindern herunterzusetzen.
    </strong>
  </p>
  <p>
    Beide Gruppen hätten nach der Steuer rund 800 Kronen weniger im Monat. „Wir
    machen uns Sorgen, weil Eltern mit kleinen Kindern nicht die gleichen
    Möglichkeiten haben neben dem Studium noch einen Job zu finden, als andere
    Studenten. Deshalb wird eine SU-Senkung diese Familien besonders hart
    treffen“, so Clausen, der die Befürchtung hat, dass sich Kinderfamilien
    mit SU künftig höher verschulden müssen.  Während seiner Präsentation
    des 2025-Plans versicherte der Finanzminister Claus Hjort Frederiksen (V),
    dass Menschen mit Handicap und Studienrende mit Kindern nicht von den
    SU-Einsparungen betroffen sein werden. Doch laut Jyllands Posten, werden
    beide Gruppen nun auch die Sparmaßnahmen zu spüren bekommen. Solange
    Arbeitgeber nicht offner dafür sind, Menschen mit Handicap einzustellen,
    sei es ein „kolossaler Fehler“, die SU für diese Menschen zu senken,
    so der Vorsitzende von Danske Handicaporganisationer, Thorkild Olesen.
  </p>
